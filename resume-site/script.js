function openPopover(button) {
  if (!popover || !popoverLabel || !openContactLink) {
    return;
  }

  activeContact = {
    value: button.dataset.contactValue,
    url: button.dataset.contactUrl
  };

  popoverLabel.textContent = activeContact.value;
  openContactLink.href = activeContact.url;
  button.classList.add("active");
  contactButtons.forEach((item) => {
    if (item !== button) {
      item.classList.remove("active");
    }
  });

  const rect = button.getBoundingClientRect();
  const popoverWidth = Math.min(280, window.innerWidth - 24);
  const left = Math.min(Math.max(12, rect.left), window.innerWidth - popoverWidth - 12);
  const top = Math.min(rect.bottom + 10, window.innerHeight - 130);

  popover.style.left = `${left}px`;
  popover.style.top = `${top}px`;
  popover.hidden = false;
}

