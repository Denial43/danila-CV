const galleryState = {};

function readSavedLanguage() {
  try {
    return localStorage.getItem("resumeLanguage");
  } catch {
    return null;
  }
}

function saveLanguage(language) {
  try {
    localStorage.setItem("resumeLanguage", language);
  } catch {
    return;
  }
}

function setLanguage(language) {
  const dictionary = translations[language] || translations.en;
  activeLanguage = translations[language] ? language : "en";
  document.documentElement.lang = activeLanguage;

  textNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === activeLanguage);
  });

  saveLanguage(activeLanguage);
  renderPlayable(activePlayable, false);
  Object.keys(galleryState).forEach((name) => renderGallery(name, galleryState[name].activeIndex, false));
}

function showSlide(index) {
  activeSlide = (index + slides.length) % slides.length;
  slides.forEach((slide, slideIndex) => {
    slide.classList.toggle("active", slideIndex === activeSlide);
  });
  dots.forEach((dot, dotIndex) => {
    dot.classList.toggle("active", dotIndex === activeSlide);
  });
}

function startCarousel() {
  if (slides.length < 2) {
    return;
  }

  window.clearInterval(carouselTimer);
  carouselTimer = window.setInterval(() => {
    showSlide(activeSlide + 1);
  }, 4300);
}

function moveCarousel(direction) {
  showSlide(activeSlide + direction);
  startCarousel();
}

function renderPlayable(index, reloadFrame = true) {
  if (!playableFrame || !playableCount || !playableTitle || !playableDescription) {
    return;
  }

  const nextPlayable = (index + playables.length) % playables.length;
  const playable = playables[nextPlayable];
  const dictionary = translations[activeLanguage] || translations.en;

  const updatePlayableText = () => {
    activePlayable = nextPlayable;
    playableCount.textContent = `${String(activePlayable + 1).padStart(2, "0")} / ${String(playables.length).padStart(2, "0")}`;
    playableTitle.textContent = playable.title;
    playableDescription.textContent = dictionary[playable.descriptionKey] || translations.en[playable.descriptionKey];
  };

  const panel = playableTitle.closest(".playable-panel");

  if (reloadFrame && panel) {
    panel.classList.add("text-switching");
    window.setTimeout(() => {
      updatePlayableText();
      panel.classList.remove("text-switching");
    }, 180);
  } else {
    updatePlayableText();
  }

  if (reloadFrame) {
    playableShell?.classList.remove("switching");
    void playableShell?.offsetWidth;
    playableShell?.classList.add("switching");
    window.setTimeout(() => {
      playableFrame.src = playable.src;
    }, 130);
  }
}

function restartPlayable() {
  if (!playableFrame) {
    return;
  }

  playableFrame.src = playables[activePlayable].src;
}

function renderGallery(name, index, animate = true) {
  const state = galleryState[name];
  const items = portfolioGalleries[name];

  if (!state || !items?.length) {
    return;
  }

  state.activeIndex = (index + items.length) % items.length;
  const item = items[state.activeIndex];

  const update = () => {
    const dictionary = translations[activeLanguage] || translations.en;
    state.image.src = item.src;
    state.image.alt = item.alt;
    state.count.textContent = `${String(state.activeIndex + 1).padStart(2, "0")} / ${String(items.length).padStart(2, "0")}`;
    state.title.textContent = dictionary[item.titleKey] || translations.en[item.titleKey] || "";
  };

  if (animate) {
    state.node.classList.add("switching");
    window.setTimeout(() => {
      update();
      state.node.classList.remove("switching");
    }, 180);
    return;
  }

  update();
}

function startGalleryTimer(name) {
  const state = galleryState[name];
  const items = portfolioGalleries[name];

  if (!state || !items || items.length < 2) {
    return;
  }

  window.clearInterval(state.timer);
  state.timer = window.setInterval(() => {
    renderGallery(name, state.activeIndex + 1);
  }, 4400);
}

